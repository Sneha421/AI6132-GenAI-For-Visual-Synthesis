def sample_dataset_pairs(
    base_source_dir: str,
    edited_source_dir: str,
    prompts_source_file: str,
    output_root_dir: str,
    num_samples: int = 100
):
    """
    Randomly samples image and prompt pairs and saves them to new directories.

    Args:
        base_source_dir: Path to the directory containing original base images.
        edited_source_dir: Path to the directory containing original edited images.
        prompts_source_file: Path to the text file containing original prompts.
        output_root_dir: The root directory where sampled data will be saved.
        num_samples: The number of pairs to sample.
    """

    # Load prompts and filter out empty lines
    if not os.path.exists(prompts_source_file):
        raise FileNotFoundError(f"Prompts file not found at: {prompts_source_file}")
    with open(prompts_source_file, "r") as f:
        all_prompts_raw = [line.strip() for line in f.readlines()]
    all_prompts = [p for p in all_prompts_raw if p] # Filter out empty strings

    # Get image file names from base source directory
    if not os.path.exists(base_source_dir):
        raise FileNotFoundError(f"Base images directory not found at: {base_source_dir}")
    all_image_files = sorted([f for f in os.listdir(base_source_dir) if os.path.isfile(os.path.join(base_source_dir, f))])

    # Create a list of (image_filename, prompt) tuples
    # Assuming base and edited images have the same filenames and prompts correspond by index
    data_pairs = []
    for i, img_file in enumerate(all_image_files):
        # Ensure we don't go out of bounds for prompts if image files are more numerous
        if i < len(all_prompts):
            # Check if corresponding edited image exists
            edited_img_path = os.path.join(edited_source_dir, img_file)
            if os.path.exists(edited_img_path):
                data_pairs.append({
                    "image_file": img_file,
                    "prompt": all_prompts[i]
                })
            else:
                print(f"Warning: Edited image not found for {img_file}. Skipping pair.")
        else:
            print(f"Warning: No non-empty prompt found for image {img_file}. Skipping pair.")

    if len(data_pairs) < num_samples:
        print(f"Warning: Only {len(data_pairs)} available valid pairs, sampling all of them instead of {num_samples}.")
        num_samples = len(data_pairs)
    elif len(data_pairs) == 0 and num_samples > 0:
        raise ValueError("No valid image-prompt pairs found after filtering empty prompts.")

    # Randomly sample pairs
    sampled_pairs = random.sample(data_pairs, num_samples)

    # Define output directories
    output_base_dir = os.path.join(output_root_dir, "base_images_sampled")
    output_edited_dir = os.path.join(output_root_dir, "edited_images_sampled")
    output_prompts_file = os.path.join(output_root_dir, "prompts_sampled.txt")

    # Create output directories
    os.makedirs(output_base_dir, exist_ok=True)
    os.makedirs(output_edited_dir, exist_ok=True)

    # Save sampled images and prompts
    with open(output_prompts_file, "w") as f_out:
        for pair in sampled_pairs:
            img_file = pair["image_file"]
            prompt = pair["prompt"]

            # Copy base image
            shutil.copy(os.path.join(base_source_dir, img_file), os.path.join(output_base_dir, img_file))
            # Copy edited image
            shutil.copy(os.path.join(edited_source_dir, img_file), os.path.join(output_edited_dir, img_file))
            # Write prompt
            f_out.write(prompt + "\n")

    print(f"Successfully sampled {num_samples} valid pairs to {output_root_dir}")

