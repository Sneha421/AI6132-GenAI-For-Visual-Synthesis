# --- Dataset Class ---
class InstructPix2PixDataset(Dataset):
    def __init__(self, base_images_dir, edited_images_dir, prompts_file, resolution=512, num_samples=None):
        self.base_images_dir = base_images_dir
        self.edited_images_dir = edited_images_dir
        self.prompts = self._load_prompts(prompts_file)
        self.image_files = sorted(os.listdir(base_images_dir))

        if num_samples is not None:
            # Ensure num_samples does not exceed available images/prompts
            self.image_files = self.image_files[:min(num_samples, len(self.image_files))]
            self.prompts = self.prompts[:min(num_samples, len(self.prompts))]


        if len(self.image_files) != len(self.prompts):
            # This warning is more critical when actual data is used.
            # For dummy data, this is expected.
            print("Warning: Number of image files and prompts do not match. Using the shorter list.")
            min_len = min(len(self.image_files), len(self.prompts))
            self.image_files = self.image_files[:min_len]
            self.prompts = self.prompts[:min_len]

        self.resolution = resolution

        self.transform = transforms.Compose([
            transforms.Resize((resolution, resolution), interpolation=transforms.InterpolationMode.BILINEAR),
            transforms.ToTensor(),
            transforms.Normalize([0.5], [0.5]),
        ])


    def _load_prompts(self, prompts_file):
        with open(prompts_file, "r") as f:
            prompts = [line.strip() for line in f.readlines()]
        return prompts

    def __len__(self):
        return len(self.image_files)

    def __getitem__(self, idx):
        base_image_name = self.image_files[idx]
        edited_image_name = self.image_files[idx] # Assuming edited images have the same name
        prompt = self.prompts[idx]

        base_image_path = os.path.join(self.base_images_dir, base_image_name)
        edited_image_path = os.path.join(self.edited_images_dir, edited_image_name)

        base_image = Image.open(base_image_path).convert("RGB")
        edited_image = Image.open(edited_image_path).convert("RGB")

        base_image_tensor = self.transform(base_image)
        edited_image_tensor = self.transform(edited_image)

        return {
            "base_image": base_image_tensor,
            "edited_image": edited_image_tensor,
            "prompt": prompt,
        }

