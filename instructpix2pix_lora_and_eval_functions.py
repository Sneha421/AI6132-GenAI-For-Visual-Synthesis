import os
import PIL
from diffusers import StableDiffusionInstructPix2PixPipeline, EulerAncestralDiscreteScheduler
import torch
from safetensors.torch import load_file

def batch_edit_with_finetuned_model(image_directory: str, prompts_file: str, output_dir: str, lora_output_dir: str):
    """
    Edits images from a directory based on prompts from a text file
    using a fine-tuned InstructPix2Pix model with LoRA weights,
    and saves the edited images.

    Args:
        image_directory: The path to the directory containing the input images.
        prompts_file: The path to the text file containing prompts, one prompt per line.
        output_dir: The path to the directory where the edited images will be saved.
        lora_output_dir: The path to the directory containing the saved LoRA weights.
    """
    if not os.path.exists(prompts_file) or not os.path.isfile(prompts_file):
        raise FileNotFoundError(f"Prompts file not found at: {prompts_file}")

    with open(prompts_file, 'r') as f:
        prompts = f.readlines()
    prompts = [prompt.strip() for prompt in prompts]

    if not os.path.exists(image_directory) or not os.path.isdir(image_directory):
        raise FileNotFoundError(f"Image directory not found at: {image_directory}")

    image_files = sorted([f for f in os.listdir(image_directory) if os.path.isfile(os.path.join(image_directory, f))])

    if len(image_files) != len(prompts):
        # Handle cases where prompts are fewer than images - perhaps repeat prompts or stop at the end of prompts
        print("Warning: The number of image files does not match the number of prompts. Using the shorter list.")
        min_len = min(len(image_files), len(prompts))
        image_files = image_files[:min_len]
        prompts = prompts[:min_len]


    os.makedirs(output_dir, exist_ok=True)

    # Load the base model
    model_id = "timbrooks/instruct-pix2pix"
    pipe = StableDiffusionInstructPix2PixPipeline.from_pretrained(
        model_id,
        torch_dtype=torch.float16,
        revision="fp16",
        safety_checker=None
    )

    # Move the pipeline to GPU if available
    if torch.cuda.is_available():
        pipe.to("cuda")

    # Load the LoRA weights manually from the saved files
    # Corrected filenames based on the debug output
    unet_lora_path = os.path.join(lora_output_dir, "unet_lora", "diffusion_pytorch_model.safetensors")
    text_encoder_lora_path = os.path.join(lora_output_dir, "text_encoder_lora", "model.safetensors")

    print(f"Checking for UNet LoRA weights at: {unet_lora_path}")
    print(f"Checking for Text Encoder LoRA weights at: {text_encoder_lora_path}")

    if not os.path.exists(unet_lora_path) or not os.path.exists(text_encoder_lora_path):
        print(f"Error: LoRA weights not found at expected paths.")
        print(f"Does {os.path.dirname(unet_lora_path)} exist? {os.path.exists(os.path.dirname(unet_lora_path))}")
        print(f"Does {os.path.dirname(text_encoder_lora_path)} exist? {os.path.exists(os.path.dirname(text_encoder_lora_path))}")
        if os.path.exists(lora_output_dir):
            print(f"Contents of {lora_output_dir}: {os.listdir(lora_output_dir)}")
            if os.path.exists(os.path.join(lora_output_dir, "unet_lora")):
                print(f"Contents of {os.path.join(lora_output_dir, 'unet_lora')}: {os.listdir(os.path.join(lora_output_dir, 'unet_lora'))}")
            if os.path.exists(os.path.join(lora_output_dir, "text_encoder_lora")):
                print(f"Contents of {os.path.join(lora_output_dir, 'text_encoder_lora')}: {os.listdir(os.path.join(lora_output_dir, 'text_encoder_lora'))}")
        else:
            print(f"LoRA output directory itself does not exist: {lora_output_dir}")
        raise FileNotFoundError(f"LoRA weights not found in {lora_output_dir}. Please ensure training was successful and weights were copied correctly to this Google Drive path.")


    unet_lora_state_dict = load_file(unet_lora_path)
    text_encoder_lora_state_dict = load_file(text_encoder_lora_path)

    # Apply the LoRA weights
    pipe.unet.load_state_dict(unet_lora_state_dict, strict=False)
    pipe.text_encoder.load_state_dict(text_encoder_lora_state_dict, strict=False)

    # Set the scheduler
    pipe.scheduler = EulerAncestralDiscreteScheduler.from_config(pipe.scheduler.config)

    print("InstructPix2Pix model with LoRA weights loaded successfully for batch inference.")


    for image_file, prompt in zip(image_files, prompts):
        image_path = os.path.join(image_directory, image_file)
        try:
            image = PIL.Image.open(image_path)
            image = PIL.ImageOps.exif_transpose(image)
            image = image.convert("RGB")

            # Perform inference with the fine-tuned model
            edited_image = pipe(
                prompt,
                image=image,
                num_inference_steps=20, # You can adjust these parameters as needed
                image_guidance_scale=1.0
            ).images[0]

            output_path = os.path.join(output_dir, image_file)
            edited_image.save(output_path)
            print(f"Processed and saved: {image_file}")
        except Exception as e:
            print(f"Warning: Could not process image {image_file}. Error: {e}")
            continue # Continue to the next image

