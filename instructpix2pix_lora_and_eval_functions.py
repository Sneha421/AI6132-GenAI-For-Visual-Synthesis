# 8. Training loop
num_train_epochs = 7
max_train_steps = num_train_epochs * len(train_dataloader)

lr_scheduler = torch.optim.lr_scheduler.ConstantLR(optimizer, factor=1.0)

progress_bar = tqdm(range(max_train_steps), desc="Training steps")
global_step = 0 # Track global steps for checkpointing

for epoch in range(num_train_epochs):
    pipe.unet.train()
    pipe.text_encoder.train()
    vae.eval()
    for step, batch in enumerate(train_dataloader):
        with accelerator.accumulate(pipe.unet, pipe.text_encoder):
            with torch.no_grad():
                base_latents = vae.encode(batch["base_image"]).latent_dist.sample()
                edited_latents = vae.encode(batch["edited_image"]).latent_dist.sample()
                base_latents = base_latents * vae.config.scaling_factor
                edited_latents = edited_latents * vae.config.scaling_factor

            prompt_ids = pipe.tokenizer(
                batch["prompt"],
                padding="max_length",
                truncation=True,
                max_length=pipe.tokenizer.model_max_length,
                return_tensors="pt",
            ).input_ids.to(accelerator.device)
            encoder_hidden_states = pipe.text_encoder(prompt_ids)[0]

            # Dynamically get the current batch size from the actual data
            current_batch_size = batch["base_image"].shape[0]

            noise = torch.randn_like(edited_latents).to(accelerator.device)
            # Use current_batch_size for timesteps generation
            timesteps = torch.randint(0, pipe.scheduler.config.num_train_timesteps, (current_batch_size,), device=accelerator.device).long()
            noisy_latents = pipe.scheduler.add_noise(edited_latents, noise, timesteps)

            model_input = torch.cat([noisy_latents, base_latents], dim=1)

            noise_pred = pipe.unet(model_input, timesteps, encoder_hidden_states).sample

            loss = torch.nn.functional.mse_loss(noise_pred.float(), noise.float(), reduction="mean")

            accelerator.backward(loss)
            optimizer.step()
            lr_scheduler.step()
            optimizer.zero_grad()

        global_step += 1
        progress_bar.update(1)
        accelerator.log({"train_loss": loss.item()}, step=epoch * len(train_dataloader) + step)

        # Checkpoint saving
        if global_step > 0 and global_step % checkpoint_steps == 0:
            accelerator.wait_for_everyone()
            unwrapped_unet_ckpt = accelerator.unwrap_model(pipe.unet)
            unwrapped_text_encoder_ckpt = accelerator.unwrap_model(pipe.text_encoder)

            checkpoint_dir = os.path.join(checkpoint_base_dir, f"step_{global_step}")
